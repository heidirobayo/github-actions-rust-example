// craete a function two add two numbers
pub fn add_two(a: i32) -> i32 {
    a + 2
}

// create a function to sub two numbers
pub fn sub(a: i32) -> i32 {
    a - 2
}

// create a function to multiply two numbers
pub fn mul(a: i32) -> i32 {
    a * 2
}

// create a function to divide two numbers
pub fn div(a: i32) -> i32 {
    a / 2
}
